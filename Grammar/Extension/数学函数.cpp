// 数学上的函数
#include <cmath>
#include <iostream>
using namespace std;

int main(){
    cout << pow(2,3) << endl;
    cout << sqrt(4) << endl;
    cout << abs(-5) << endl;
    cout << fabs(-10.5) << endl;
    cout << ceil(1.1) << endl;
    cout << floor(1.1) << endl;
    cout << round(4.5) << endl;
    return 0;
}